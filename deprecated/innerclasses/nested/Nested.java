package demo.innerclasses.nested;

import java.util.Comparator;

public class Nested {

    public static class NumberAscComparator implements Comparator<Integer> {
        @Override
        public int compare(Integer o1, Integer o2) {
            if (o1 > o2) {
                return 1;
            } else if (o1 < o2) {
                return -1;
            } else {
                return 0;
            }
        }
    }
}
